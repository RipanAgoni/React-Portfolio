const ParticlesBackground = () => { // Renamed component for clarity
  const [particleCount, setParticleCount] = useState('--');

  useEffect(() => {
    const script = document.createElement('script');
    script.src = 'https://cdn.jsdelivr.net/particles.js/2.0.0/particles.min.js';
    script.async = true;
    script.onload = () => {
      if (window.particlesJS) {
        window.particlesJS('particles-js', particlesConfig);

        const interval = setInterval(() => {
          if (window.pJSDom && window.pJSDom[0] && window.pJSDom[0].pJS && window.pJSDom[0].pJS.particles && window.pJSDom[0].pJS.particles.array) {
            setParticleCount(window.pJSDom[0].pJS.particles.array.length);
          }
        }, 1000);

        return () => clearInterval(interval);
      }
    };
    document.body.appendChild(script);

    return () => {
      if (document.body.contains(script)) {
        document.body.removeChild(script);
      }
    };
  }, []);

  return (
    <div className="absolute inset-0 w-full h-screen bg-transparent z-[-1]"> {/* Set z-index to -1 */}
      <div id="particles-js" className="w-full h-full"></div>
    </div>
  );
};

export default ParticlesBackground;